<?php

/**
 * ModuleLogout - Wylogowanie
 */
class ModuleLogout implements ShellInterface
{
	/**
	 * Obiekt Shell
	 *
	 * @access private
	 * @var    object
	 */
	private $oShell;

	/**
	 * Konstruktor
	 *
	 * @access public
	 * @param  object $oShell Obiekt Shell
	 * @return void
	 */
	public function __construct( Shell $oShell )
	{
		$this -> oShell = $oShell;
	}

	/**
	 * Dostepna lista komend
	 *
	 * @access public
	 * @return array
	 */
	public function getCommands()
	{
		return array( 'logout' );
	}

	/**
	 * Zwracanie wersji modulu
	 *
	 * @access public
	 * @return string
	 */
	public function getVersion()
	{
		/**
		 * Wersja Data Autor
		 */
		return '1.0 2011-06-19 - <krzotr@gmail.com>';
	}

	/**
	 * Zwracanie pomocy modulu
	 *
	 * @access public
	 * @return string
	 */
	public function getHelp()
	{
		return <<<DATA
Wylogowanie

	Użycie:
		logout
DATA;
	}

	/**
	 * Wywolanie modulu
	 *
	 * @access public
	 * @return string
	 */
	public function get()
	{
		/**
		 * Czy jest sie zalogowanym
		 */
		if( isset( $_SESSION['auth'] ) && ( $_SESSION['auth'] === sha1( $this -> oShell -> sAuth . Request::getServer( 'REMOTE_ADDR' ) ) ) )
		{
			$_SESSION['auth'] = NULL;
			unset( $_SESSION['auth'] );

			return 'Zostałes wylogowany';
		}

		return 'Nie jesteś zalogowany, więc nie możesz się wylogować !!!';
	}

}